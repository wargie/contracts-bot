    """
    Attributes:
        query (str):
        count (int | None | Unset):  Default: 10.
    """

    query: str
    count: int | None | Unset = 10
    additional_properties: dict[str, Any] = _attrs_field(init=False, factory=dict)

    def to_dict(self) -> dict[str, Any]:
        query = self.query

        count: int | None | Unset
